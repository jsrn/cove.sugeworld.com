			      <B><U>Militia Orders</U></B><BR>
			      <U>Basic Orders</U><BR>
			      <I><B>Fall In</B></I> : All guardsmen will form a line two paces in front
			      of the person giving the order.<BR>
			      <I><B>Arm Up</B></I> : All guardsmen will brandish their weapon, mages will
			      reveal their spellbook.<BR>
			      <B><I>Ready Arms</I> </B>: All guardsmen will get into a combat stance, ready
			      for battle (i.e. Go into war mode). This can also be accompined by a suitable
			      emote; *gets ready for battle*, etc.<BR>
			      <I><B>At Ease</B></I> : All guardsmen will come out of combat stance, and
			      return to normal (i.e. go out of war mode). This, again, can be accompined
			      by a suitable emote; *stands easy*, etc. This order can also be used to tell
			      a guardsmen to stop attacking, and generally take a few steps back if being
			      insulted or provoked.
			      <P>
			      <U>Advanced Orders</U><BR>
			      <B><I>Tracker's Line</I> </B>: All guardsmen will form behind the person
			      giving the order, facing his back, and await further instructions.<BR>
			      <I><B>Hide!</B></I> : All guardsmen will run to a space and hide (if you
			      have no actual hiding skill, run off screen or emote *hide's behind tree*,etc).
			      <P>
			      <B><U>Roleplaying</U></B><BR>
			      Cove has a wide variety of positions to suit everyone's style of roleplay,
			      from the lowest peasant to the highest Captain of the Baron's armed forces.
			      They are all united in their common goals; to survive and to prosper. Beyond
			      that the ambitions of the character is down to the player, although a few
			      points that are in common roleplay throughout all of the citizens of Cove
			      are; being suspicious of outsiders (as they are a close knit community),
			      and believing the Baron to hold absolute power (and thus never questioning
			      him or failing to follow his command).
			      <P>
			      <B><U>Location of Baronship</U></B><BR>
			      Simply follow the coast north from the town of Cove to find the residence
			      of Baron Octiovus.<BR>
